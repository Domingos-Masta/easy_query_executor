    private void jListDatabasesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListDatabasesMouseClicked
        // TODO add your handling code here:
        String tabList[];
        if (evt.getButton() == MouseEvent.BUTTON1 && evt.getClickCount() >= 2)
        {
            if (jListDatabases.getSelectedValue() != null)
            {
                jListDatabases.setEnabled(false);
                useDatabaseBtn.setEnabled(false);
                changeDbBtn.setEnabled(true);
                refreshBtn.setEnabled(false);
                createDatabaseQbtn.setEnabled(false);
                refreshQbtn.setEnabled(false);
                Conexao.setDatabase(jListDatabases.getSelectedValue().toString().trim());

                tabList = Executor.getDatabaseTable();
                listaDeTabelas.removeAll();
                listaDeTabelas.setListData(tabList);
                listaProceduresJl.removeAll();
                listaProceduresJl.setListData(Executor.getDatabaseProcedures());
                listaDeTriggers.removeAll();
                listaDeTriggers.setListData(Executor.getDatabaseTriggers());

            }
            else
            {
                JOptionPane.showMessageDialog(null, "Erro Nenhum item Selecionado");
            }
        }
        
    }//GEN-LAST:event_jListDatabasesMouseClicked

