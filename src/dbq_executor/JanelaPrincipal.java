    private void useDatabaseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_useDatabaseBtnActionPerformed
        // TODO add your handling code here:
        
        String tabList[];
        
        if (jListDatabases.getSelectedValue() != null)
        {
            jListDatabases.setEnabled(false);
            useDatabaseBtn.setEnabled(false);
            changeDbBtn.setEnabled(true);
            refreshBtn.setEnabled(false);
            createDatabaseQbtn.setEnabled(false);
            refreshQbtn.setEnabled(false);
            Conexao.setDatabase(jListDatabases.getSelectedValue().toString().trim());
            
            tabList = Executor.getDatabaseTable();
            listaDeTabelas.removeAll();
            listaDeTabelas.setListData(tabList);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Erro Nenhum item Selecionado");
        }
    }//GEN-LAST:event_useDatabaseBtnActionPerformed

    private void changeDbBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeDbBtnActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(null, " Não avera nenhuma base de dados Selecionada!\n "
                + "Continuar?") == 0)
        {
            jListDatabases.setEnabled(true);
            useDatabaseBtn.setEnabled(true);
            changeDbBtn.setEnabled(false);
            refreshBtn.setEnabled(true);
            createDatabaseQbtn.setEnabled(true);
            refreshQbtn.setEnabled(true);
            Conexao.setDatabase("");
            listaDeTabelas.setListData(new String[1]);
            listaDeTabelas.updateUI();
        }
    }//GEN-LAST:event_changeDbBtnActionPerformed

